class QuoteLocators:
    """A class that contains the quote CSS locators.
    
    Parameters
    ----------
    AUTHOR : str
        The CSS locator that selects the small tag of class author.
    CONTENT : str
        The CSS locator that selects the span tag of class text.
    TAGS : str
        The CSS locator that selects the div tag of class tags.
    """
    AUTHOR = "small.author"
    CONTENT = "span.text"
    TAGS = "div.tags"